let enemy

function Enemy(
  enemyType,
  health,
  mana,
  strength,
  agility,
  intelligence,
  speed,
  resistance,
  defense
) {
  this.enemyType = enemyType
  this.health = health
  this.mana = mana
  this.strength = strength
  this.agility = agility
  this.intelligence = intelligence
  this.speed = speed
  this.resistance = resistance
  this.defense = defense
}
